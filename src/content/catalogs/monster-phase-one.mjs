function defineArchetypeMonsters(archetypeId, monsters) {
  return monsters.map((monster) => ({ archetypeId, ...monster }));
}

function clampStat(value) {
  return Math.max(1, value);
}

function cloneGrammar(profile) {
  return {
    ...profile,
    ...(profile.inflectableParts
      ? {
          inflectableParts: {
            ...profile.inflectableParts,
          },
        }
      : {}),
  };
}

function createGrammar(spec) {
  const base = spec.gender === "feminine"
    ? GRAMMAR.GENERIC_FEMININE
    : spec.gender === "neuter"
      ? GRAMMAR.GENERIC_NEUTER
      : GRAMMAR.GENERIC_MASCULINE;

  if (!spec.inflectable) {
    return cloneGrammar(base);
  }

  return {
    ...cloneGrammar(base),
    inflectableParts: {
      adjectiveStem: spec.inflectable.adjectiveStem,
      noun: spec.inflectable.noun,
    },
  };
}

function buildDescription(spec) {
  const role = String(spec.role ?? "Gegner").trim();
  const roleText = role ? role.charAt(0).toUpperCase() + role.slice(1) : "Gegner";
  return `${roleText} aus ${ARCHETYPE_FLAVORS[spec.archetypeId] ?? "einem Filmset voller Unruhe"}.`;
}

function buildSpecial(spec) {
  const parts = [BEHAVIOR_SPECIALS[spec.behavior] ?? "Reagiert aggressiv, sobald es dich wahrnimmt."];

  if (spec.mobility === "relentless") {
    parts.push("Verliert über Raumwechsel nur schwer den Zugriff.");
  } else if (spec.mobility === "local") {
    parts.push("Bleibt bevorzugt in seinem Revier und macht dort dicht.");
  }

  if (spec.retreatProfile === "cowardly") {
    parts.push("Weicht bei ungünstigen Schlagabtauschen früh zurück.");
  } else if (spec.retreatProfile === "cautious") {
    parts.push("Sucht bei schlechtem Stand kontrolliert Abstand.");
  }

  if (spec.healingProfile === "steady") {
    parts.push("Erholt sich auch außerhalb kurzer Ruhefenster verlässlich.");
  } else if (spec.healingProfile === "lurking") {
    parts.push("Kommt vor allem wieder zu Kräften, wenn der Druck kurz abreißt.");
  }

  return parts.join(" ");
}

function buildStats(rank, roleProfileId) {
  const baseline = RANK_BASELINES[rank] ?? RANK_BASELINES[1];
  const roleProfile = ROLE_PROFILES[roleProfileId] ?? ROLE_PROFILES.hunter;
  const statMods = roleProfile.statMods;

  return {
    hp: clampStat(baseline.hp + (statMods.hp ?? 0)),
    strength: clampStat(baseline.strength + (statMods.strength ?? 0)),
    precision: clampStat(baseline.precision + (statMods.precision ?? 0)),
    reaction: clampStat(baseline.reaction + (statMods.reaction ?? 0)),
    nerves: clampStat(baseline.nerves + (statMods.nerves ?? 0)),
    intelligence: clampStat(baseline.intelligence + (statMods.intelligence ?? 0)),
    xpReward: clampStat(baseline.xpReward),
  };
}

function buildSpawnWeight(rank, roleProfileId, explicitWeight) {
  if (Number.isFinite(explicitWeight)) {
    return Math.max(1, Math.round(explicitWeight));
  }

  const baseWeight = SPAWN_WEIGHT_BY_RANK[rank] ?? 2;
  const multiplier = ROLE_PROFILES[roleProfileId]?.spawnWeightMultiplier ?? 1;
  return Math.max(1, Math.round(baseWeight * multiplier));
}

function createStandardMonster(spec) {
  const stats = buildStats(spec.rank, spec.roleProfile);

  return {
    id: spec.id,
    name: spec.name,
    archetypeId: spec.archetypeId,
    spawnGroup: "standard",
    spawnWeight: buildSpawnWeight(spec.rank, spec.roleProfile, spec.spawnWeight),
    grammar: createGrammar(spec),
    rank: spec.rank,
    behavior: spec.behavior,
    behaviorLabel: spec.behaviorLabel,
    description: spec.description ?? buildDescription(spec),
    mobility: spec.mobility,
    mobilityLabel: MOBILITY_LABELS[spec.mobility],
    retreatProfile: spec.retreatProfile,
    retreatLabel: RETREAT_LABELS[spec.retreatProfile],
    healingProfile: spec.healingProfile,
    healingLabel: HEALING_LABELS[spec.healingProfile],
    allowedTemperaments: [...spec.allowedTemperaments],
    strength: stats.strength,
    precision: stats.precision,
    reaction: stats.reaction,
    nerves: stats.nerves,
    intelligence: stats.intelligence,
    hp: stats.hp,
    xpReward: stats.xpReward,
    aggroRadius: spec.aggroRadius,
    canOpenDoors: Boolean(spec.canOpenDoors),
    canChangeFloors: Boolean(spec.canChangeFloors),
    special: spec.special ?? buildSpecial(spec),
    preferredWeaponRoles: Array.isArray(spec.preferredWeaponRoles) ? [...spec.preferredWeaponRoles] : undefined,
  };
}

