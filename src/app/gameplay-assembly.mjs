export function assembleGameplayModules(context) {
  const {
    createCombatApi,
    BASE_HIT_CHANCE,
    MIN_HIT_CHANCE,
    MAX_HIT_CHANCE,
    MIN_CRIT_CHANCE,
    MAX_CRIT_CHANCE,
    clamp,
    rollPercent,
    getState,
    getCombatWeapon,
    getOffHand,
    getCurrentFloorState,
    createWeaponPickup,
    createOffHandPickup,
    createFoodPickup,
    showFloatingText,
    playVictorySound,
    playLevelUpSound,
    playEnemyHitSound,
    playDodgeSound,
    xpForNextLevel,
    getLevelUpRewards,
    getWeaponConditionalDamageBonus,
    itemHasModifier,
    noteMonsterEncounter,
    addMessage,
    renderSelf,
    createAiApi,
    WIDTH,
    HEIGHT,
    TILE,
    DOOR_TYPE,
    getDoorAt,
    createDeathCause,
    playPlayerHitSound,
    playDeathSound,
    playDoorOpenSound,
    saveHighscoreIfNeeded,
    showDeathModal,
    handleActorEnterTile,
    createItemsApi,
    getMainHand,
    applyItemStatMods,
    cloneWeapon,
    cloneOffHandItem,
    formatWeaponStats,
    formatOffHandStats,
    formatRarityLabel,
    showChoiceModal,
    hideChoiceModal,
    endTurn,
    restoreNutrition,
    refreshNutritionState,
    setResolvePotionChoice,
    setUseInventoryItem,
    setQuickUsePotion,
    createFloorTransitionService,
    createDungeonLevel,
    detectNearbyTraps,
    maybeTriggerShowcaseAmbience,
    formatStudioLabel,
    formatArchetypeLabel,
    showStairChoice,
    createPlayerTurnController,
    getShowcaseAt,
    openDoor,
    closeDoor,
    canPlayerOpenDoor,
    getDoorColorLabels,
    playStepSound,
    playLockedDoorSound,
    processContinuousTraps,
    applyPlayerNutritionTurnCost,
    createTestApi,
    countPotionsInInventory,
    countFoodInInventory,
    loadHighscores,
    createChestPickup,
    createDoor,
    createKeyPickup,
    generateEquipmentItem,
    setRandomSequence,
    clearRandomSequence,
    tryUseStairs,
    setMoveToFloor,
    setTryUseStairs,
    setEndTurn,
    setTryCloseAdjacentDoor,
    setMovePlayer,
    setHandleWait,
  } = context;

  const combatApi = createCombatApi({
    BASE_HIT_CHANCE,
    MIN_HIT_CHANCE,
    MAX_HIT_CHANCE,
    MIN_CRIT_CHANCE,
    MAX_CRIT_CHANCE,
    clamp,
    rollPercent,
    getState,
    getCombatWeapon,
    getOffHand,
    getCurrentFloorState,
    createWeaponPickup,
    createOffHandPickup,
    createFoodPickup,
    showFloatingText,
    playVictorySound,
    playLevelUpSound,
    playEnemyHitSound,
    playDodgeSound,
    xpForNextLevel,
    getLevelUpRewards,
    getWeaponConditionalDamageBonus,
    itemHasModifier,
    noteMonsterEncounter,
    addMessage,
    renderSelf,
  });

  const aiApi = createAiApi({
    WIDTH,
    HEIGHT,
    TILE,
    DOOR_TYPE,
    getState,
    getCurrentFloorState,
    getDoorAt,
    getOffHand,
    resolveCombatAttack: combatApi.resolveCombatAttack,
    resolveBlock: combatApi.resolveBlock,
    healPlayer: combatApi.healPlayer,
    addMessage,
    showFloatingText,
    createDeathCause,
    playPlayerHitSound,
    playDodgeSound,
    playDeathSound,
    playDoorOpenSound,
    saveHighscoreIfNeeded,
    showDeathModal,
    noteMonsterEncounter,
    handleActorEnterTile,
  });

  const itemsApi = createItemsApi({
    getState,
    getCurrentFloorState,
    getMainHand,
    getOffHand,
    applyItemStatMods,
    cloneWeapon,
    cloneOffHandItem,
    createWeaponPickup,
    createOffHandPickup,
    createFoodPickup,
    formatWeaponStats,
    formatOffHandStats,
    formatRarityLabel,
    addMessage,
    showChoiceModal,
    hideChoiceModal,
    endTurn,
    healPlayer: combatApi.healPlayer,
    restoreNutrition,
    refreshNutritionState,
    renderSelf,
  });
  setResolvePotionChoice(itemsApi.resolvePotionChoice);
  setUseInventoryItem(itemsApi.useInventoryItem);
  setQuickUsePotion(itemsApi.quickUsePotion);

  const floorTransitionService = createFloorTransitionService({
    getState,
    getCurrentFloorState,
    createDungeonLevel,
    detectNearbyTraps,
    maybeTriggerShowcaseAmbience,
    manhattanDistance: aiApi.manhattanDistance,
    addMessage,
    formatStudioLabel,
    formatArchetypeLabel,
    showStairChoice,
    renderSelf,
  });

  const playerTurnController = createPlayerTurnController({
    WIDTH,
    HEIGHT,
    TILE,
    DOOR_TYPE,
    getState,
    getCurrentFloorState,
    getDoorAt,
    getShowcaseAt,
    openDoor,
    closeDoor,
    canPlayerOpenDoor,
    getDoorColorLabels,
    manhattanDistance: aiApi.manhattanDistance,
    addMessage,
    attackEnemy: combatApi.attackEnemy,
    tryPickupLoot: itemsApi.tryPickupLoot,
    tryUseStairs: floorTransitionService.tryUseStairs,
    detectNearbyTraps,
    maybeTriggerShowcaseAmbience,
    handleActorEnterTile,
    playStepSound,
    playLockedDoorSound,
    hasNearbyEnemy: aiApi.hasNearbyEnemy,
    moveEnemies: aiApi.moveEnemies,
    processContinuousTraps,
    processSafeRegeneration: aiApi.processSafeRegeneration,
    applyPlayerNutritionTurnCost,
    renderSelf,
  });

  const testApi = createTestApi({
    WIDTH,
    HEIGHT,
    TILE,
    getState,
    getCurrentFloorState,
    getMainHand,
    getOffHand,
    countPotionsInInventory,
    countFoodInInventory,
    loadHighscores,
    grantExperience: combatApi.grantExperience,
    cloneWeapon,
    cloneOffHandItem,
    createChestPickup,
    createFoodPickup,
    createDoor,
    createKeyPickup,
    generateEquipmentItem,
    setRandomSequence,
    clearRandomSequence,
    tryUseStairs,
    renderSelf,
  });

  setMoveToFloor(floorTransitionService.moveToFloor);
  setTryUseStairs(floorTransitionService.tryUseStairs);
  setEndTurn(playerTurnController.endTurn);
  setTryCloseAdjacentDoor(playerTurnController.tryCloseAdjacentDoor);
  setMovePlayer(playerTurnController.movePlayer);
  setHandleWait(playerTurnController.handleWait);

  return {
    ...combatApi,
    ...aiApi,
    ...itemsApi,
    ...floorTransitionService,
    ...playerTurnController,
    ...testApi,
  };
}
