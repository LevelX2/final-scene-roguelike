export function assembleInterfaceModules(context) {
  const { factories, config, runtime, ui, equipment, core, presentation } = context;
  const {
    createRenderApi,
    createModalController,
    createInputController,
  } = factories;
  const {
    MIN_CRIT_CHANCE,
    MAX_CRIT_CHANCE,
    WIDTH,
    HEIGHT,
    TILE_SIZE,
    TILE_GAP,
    BOARD_PADDING,
    TILE,
    MONSTER_CATALOG,
    WEAPON_CATALOG,
    OFFHAND_CATALOG,
    ITEM_DEFS,
    CHOICE_ACTIONS,
    STAIR_ACTIONS,
  } = config;
  const {
    getState,
    getCurrentFloorState,
    clamp,
    getHungerStateLabel,
    countPotionsInInventory,
    countFoodInInventory,
    useInventoryItem,
    renderSelf,
    setShowFloatingText,
    syncStartModalControls,
    addMessage,
    moveToFloor,
    endTurn,
    resolvePotionChoice,
    setShowDeathModal,
    setShowChoiceModal,
    setHideChoiceModal,
    movePlayer,
    handleWait,
    tryCloseAdjacentDoor,
    quickUsePotion,
  } = runtime;
  const {
    getMainHand,
    getOffHand,
    getCombatWeapon,
  } = equipment;
  const {
    formatWeaponStats,
    formatOffHandStats,
    getOffHandTooltipLines,
  } = presentation;
  const {
    formatRarityLabel,
    getItemModifierSummary,
    loadHighscores,
    loadLastHighscoreMarker,
    clearSavedGame,
    updateSavegameControls,
    initializeGame,
  } = core;
  const {
    boardElement,
    messageLogElement,
    inventoryListElement,
    inventoryFilterButtons,
    playerSheetElement,
    enemySheetElement,
    highscoreListElement,
    runStatsSummaryElement,
    runStatsKillsElement,
    hoverTooltipElement,
    choiceModalElement,
    choiceTitleElement,
    choiceTextElement,
    choiceDrinkButton,
    choiceStoreButton,
    choiceLeaveButton,
    stairsModalElement,
    stairsTitleElement,
    stairsTextElement,
    stairsConfirmButton,
    stairsStayButton,
    deathModalElement,
    deathSummaryElement,
    deathKillsElement,
  } = ui;

  const renderApi = createRenderApi({
    MIN_CRIT_CHANCE,
    MAX_CRIT_CHANCE,
    WIDTH,
    HEIGHT,
    TILE_SIZE,
    TILE_GAP,
    BOARD_PADDING,
    TILE,
    boardElement,
    messageLogElement,
    inventoryListElement,
    inventoryFilterButtons,
    playerSheetElement,
    enemySheetElement,
    highscoreListElement,
    runStatsSummaryElement,
    runStatsKillsElement,
    hoverTooltipElement,
    monsterNames: MONSTER_CATALOG.map((monster) => monster.name),
    itemNames: [
      "Heiltrank",
      ...Object.values(WEAPON_CATALOG).map((item) => item.name),
      ...Object.values(OFFHAND_CATALOG).map((item) => item.name),
      ...Object.values(ITEM_DEFS).map((item) => item.name),
    ],
    getState,
    getCurrentFloorState,
    getMainHand,
    getOffHand,
    getCombatWeapon,
    formatWeaponStats,
    formatOffHandStats,
    getOffHandTooltipLines,
    formatRarityLabel,
    getItemModifierSummary,
    getHungerStateLabel,
    clamp,
    loadHighscores,
    loadLastHighscoreMarker,
    countPotionsInInventory,
    countFoodInInventory,
    useInventoryItem,
    renderSelf,
  });
  setShowFloatingText(renderApi.showFloatingText);

  const modalController = createModalController({
    CHOICE_ACTIONS,
    STAIR_ACTIONS,
    getState,
    clearSavedGame,
    createSheetRow: renderApi.createSheetRow,
    updateSavegameControls,
    initializeGame,
    syncStartModalControls,
    renderSelf,
    addMessage,
    moveToFloor,
    endTurn,
    resolvePotionChoice,
    choiceModalElement,
    choiceTitleElement,
    choiceTextElement,
    choiceDrinkButton,
    choiceStoreButton,
    choiceLeaveButton,
    stairsModalElement,
    stairsTitleElement,
    stairsTextElement,
    stairsConfirmButton,
    stairsStayButton,
    deathModalElement,
    deathSummaryElement,
    deathKillsElement,
  });
  setShowDeathModal(modalController.showDeathModal);
  setShowChoiceModal(modalController.showChoiceModal);
  setHideChoiceModal(modalController.hideChoiceModal);

  const inputController = createInputController({
    getState,
    confirmRestartRun: modalController.confirmRestartRun,
    resolveStairChoice: modalController.resolveStairChoice,
    cycleStairChoice: modalController.cycleStairChoice,
    resolvePotionChoice,
    cyclePotionChoice: modalController.cyclePotionChoice,
    toggleInventory: modalController.toggleInventory,
    toggleRunStats: modalController.toggleRunStats,
    toggleOptions: modalController.toggleOptions,
    toggleHelp: modalController.toggleHelp,
    toggleHighscores: modalController.toggleHighscores,
    toggleDeathKills: modalController.toggleDeathKills,
    movePlayer,
    handleWait,
    tryCloseAdjacentDoor,
    quickUsePotion,
  });

  return {
    ...renderApi,
    ...modalController,
    ...inputController,
  };
}
