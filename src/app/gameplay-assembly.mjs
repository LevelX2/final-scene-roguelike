import { createStatusEffectService } from '../application/status-effect-service.mjs';
import { getEffectStateLabel } from '../content/catalogs/weapon-effects.mjs';

export function assembleGameplayModules(context) {
  const { factories, config, runtime, equipment, core, interface: interfaceApi, presentation } = context;
  const {
    createCombatApi,
    createAiApi,
    createItemsApi,
    createFloorTransitionService,
    createPlayerTurnController,
    createTestApi,
  } = factories;
  const {
    BASE_HIT_CHANCE,
    MIN_HIT_CHANCE,
    MAX_HIT_CHANCE,
    MIN_CRIT_CHANCE,
    MAX_CRIT_CHANCE,
    WIDTH,
    HEIGHT,
    TILE,
    DOOR_TYPE,
    getLevelUpRewards,
    formatStudioLabel,
    formatArchetypeLabel,
    buildStudioAnnouncement,
    getArchetypeForFloor,
  } = config;
  const {
    getState,
    getCurrentFloorState,
    clamp,
    randomChance,
    rollPercent,
    renderSelf,
    noteMonsterEncounter,
    addMessage,
    showFloatingText,
    applyItemStatMods,
    restoreNutrition,
    refreshNutritionState,
    setResolvePotionChoice,
    setUseInventoryItem,
    setQuickUsePotion,
    maybeTriggerShowcaseAmbience,
    getShowcaseAt,
    applyPlayerNutritionTurnCost,
    setRandomSequence,
    clearRandomSequence,
    tryUseStairs,
    endTurn,
    setMoveToFloor,
    setTryUseStairs,
    setEndTurn,
    setTryCloseAdjacentDoor,
    setMovePlayer,
    setHandleWait,
    countPotionsInInventory,
    countFoodInInventory,
  } = runtime;
  const {
    getMainHand,
    getOffHand,
    getCombatWeapon,
    cloneOffHandItem,
  } = equipment;
  const {
    createWeaponPickup,
    createOffHandPickup,
    createFoodPickup,
    createDungeonLevel,
    detectNearbyTraps,
    getDoorAt,
    openDoor,
    closeDoor,
    canPlayerOpenDoor,
    getDoorColorLabels,
    createDeathCause,
    saveHighscoreIfNeeded,
    loadHighscores,
    createChestPickup,
    createDoor,
    createKeyPickup,
    generateEquipmentItem,
    processContinuousTraps,
    playVictorySound,
    playLevelUpSound,
    playEnemyHitSound,
    playPlayerHitSound,
    playDodgeSound,
    playDeathSound,
    playDoorOpenSound,
    playLockedDoorSound,
    playStepSound,
    xpForNextLevel,
    formatRarityLabel,
    getItemModifierSummary,
    getWeaponConditionalDamageBonus,
    itemHasModifier,
    cloneWeapon,
    handleActorEnterTile,
  } = core;
  const {
    showDeathModal,
    showChoiceModal,
    hideChoiceModal,
    showStairChoice,
  } = interfaceApi;
  const {
    formatWeaponDisplayName,
    formatWeaponReference,
    formatWeaponStats,
    formatOffHandStats,
  } = presentation;

  let combatApi = null;

  const statusEffectService = createStatusEffectService({
    getEffectStateLabel,
    getState,
    getCurrentFloorState,
    addMessage,
    showFloatingText,
    noteMonsterEncounter,
    playEnemyHitSound,
    playPlayerHitSound,
    saveHighscoreIfNeeded,
    createDeathCause,
    showDeathModal,
    playDeathSound,
    grantExperience: (...args) => combatApi?.grantExperience?.(...args),
    randomChance,
  });

  combatApi = createCombatApi({
    BASE_HIT_CHANCE,
    MIN_HIT_CHANCE,
    MAX_HIT_CHANCE,
    MIN_CRIT_CHANCE,
    MAX_CRIT_CHANCE,
    clamp,
    randomChance,
    rollPercent,
    getState,
    getCombatWeapon,
    getOffHand,
    getCurrentFloorState,
    createWeaponPickup,
    createOffHandPickup,
    createFoodPickup,
    showFloatingText,
    playVictorySound,
    playLevelUpSound,
    playEnemyHitSound,
    playDodgeSound,
    xpForNextLevel,
    getLevelUpRewards,
    getWeaponConditionalDamageBonus,
    itemHasModifier,
    getActorPrecisionModifier: statusEffectService.getActorPrecisionModifier,
    getActorReactionModifier: statusEffectService.getActorReactionModifier,
    tryApplyWeaponEffects: statusEffectService.tryApplyWeaponEffects,
    noteMonsterEncounter,
    formatWeaponReference,
    addMessage,
    renderSelf,
  });

  const aiApi = createAiApi({
    WIDTH,
    HEIGHT,
    TILE,
    DOOR_TYPE,
    getState,
    getCurrentFloorState,
    getDoorAt,
    getOffHand,
    resolveCombatAttack: combatApi.resolveCombatAttack,
    resolveBlock: combatApi.resolveBlock,
    healPlayer: combatApi.healPlayer,
    addMessage,
    showFloatingText,
    createDeathCause,
    playPlayerHitSound,
    playDodgeSound,
    playDeathSound,
    playDoorOpenSound,
    saveHighscoreIfNeeded,
    showDeathModal,
    noteMonsterEncounter,
    handleActorEnterTile,
    randomChance,
    hasLineOfSight: core.hasLineOfSight,
    isStraightShot: core.isStraightShot,
    canActorMove: statusEffectService.canActorMove,
    tryApplyWeaponEffects: statusEffectService.tryApplyWeaponEffects,
  });

  const itemsApi = createItemsApi({
    getState,
    getCurrentFloorState,
    getMainHand,
    getOffHand,
    applyItemStatMods,
    cloneWeapon,
    cloneOffHandItem,
    createWeaponPickup,
    createOffHandPickup,
    createFoodPickup,
    formatWeaponDisplayName,
    formatWeaponReference,
    formatWeaponStats,
    formatOffHandStats,
    formatRarityLabel,
    getItemModifierSummary,
    addMessage,
    showChoiceModal,
    hideChoiceModal,
    endTurn,
    healPlayer: combatApi.healPlayer,
    restoreNutrition,
    refreshNutritionState,
    renderSelf,
  });
  setResolvePotionChoice(itemsApi.resolvePotionChoice);
  setUseInventoryItem(itemsApi.useInventoryItem);
  setQuickUsePotion(itemsApi.quickUsePotion);

  const floorTransitionService = createFloorTransitionService({
    getState,
    getCurrentFloorState,
    createDungeonLevel,
    detectNearbyTraps,
    maybeTriggerShowcaseAmbience,
    manhattanDistance: aiApi.manhattanDistance,
    addMessage,
    formatStudioLabel,
    formatArchetypeLabel,
    buildStudioAnnouncement,
    getArchetypeForFloor,
    playStudioAnnouncement: core.playStudioAnnouncement,
    showStairChoice,
    renderSelf,
  });

  const playerTurnController = createPlayerTurnController({
    WIDTH,
    HEIGHT,
    TILE,
    DOOR_TYPE,
    getState,
    getCurrentFloorState,
    getDoorAt,
    getShowcaseAt,
    openDoor,
    closeDoor,
    canPlayerOpenDoor,
    getDoorColorLabels,
    manhattanDistance: aiApi.manhattanDistance,
    addMessage,
    attackEnemy: combatApi.attackEnemy,
    tryPickupLoot: itemsApi.tryPickupLoot,
    tryUseStairs: floorTransitionService.tryUseStairs,
    detectNearbyTraps,
    maybeTriggerShowcaseAmbience,
    handleActorEnterTile,
    playStepSound,
    playLockedDoorSound,
    hasNearbyEnemy: aiApi.hasNearbyEnemy,
    moveEnemies: aiApi.moveEnemies,
    hasLineOfSight: core.hasLineOfSight,
    isStraightShot: core.isStraightShot,
    getCombatWeapon,
    canActorMove: statusEffectService.canActorMove,
    processRoundStatusEffects: statusEffectService.processRoundStatusEffects,
    processContinuousTraps,
    processSafeRegeneration: aiApi.processSafeRegeneration,
    applyPlayerNutritionTurnCost,
    renderSelf,
  });

  const testApi = createTestApi({
    WIDTH,
    HEIGHT,
    TILE,
    getState,
    getCurrentFloorState,
    getMainHand,
    getOffHand,
    countPotionsInInventory,
    countFoodInInventory,
    loadHighscores,
    grantExperience: combatApi.grantExperience,
    cloneWeapon,
    cloneOffHandItem,
    createChestPickup,
    createFoodPickup,
    createDoor,
    createKeyPickup,
    generateEquipmentItem,
    enterTargetMode: playerTurnController.enterTargetMode,
    cancelTargetMode: playerTurnController.cancelTargetMode,
    moveTargetCursor: playerTurnController.moveTargetCursor,
    confirmTargetAttack: playerTurnController.confirmTargetAttack,
    applyStatusEffect: statusEffectService.applyStatusEffect,
    processRoundStatusEffects: statusEffectService.processRoundStatusEffects,
    setRandomSequence,
    clearRandomSequence,
    tryUseStairs,
    renderSelf,
  });

  setMoveToFloor(floorTransitionService.moveToFloor);
  setTryUseStairs(floorTransitionService.tryUseStairs);
  setEndTurn(playerTurnController.endTurn);
  setTryCloseAdjacentDoor(playerTurnController.tryCloseAdjacentDoor);
  setMovePlayer(playerTurnController.movePlayer);
  setHandleWait(playerTurnController.handleWait);

  return {
    ...combatApi,
    ...aiApi,
    ...itemsApi,
    ...floorTransitionService,
    ...playerTurnController,
    ...statusEffectService,
    ...testApi,
  };
}
