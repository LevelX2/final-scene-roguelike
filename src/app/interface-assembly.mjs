import { renderEquipmentCompareHtml } from '../ui/item-choice-view.mjs';

export function assembleInterfaceModules(context) {
  const { factories, config, runtime, ui, equipment, core, presentation } = context;
  const {
    createRenderApi,
    createModalController,
    createInputController,
  } = factories;
  const {
    MIN_CRIT_CHANCE,
    MAX_CRIT_CHANCE,
    WIDTH,
    HEIGHT,
    TILE_SIZE,
    TILE_GAP,
    BOARD_PADDING,
    TILE,
    MONSTER_CATALOG,
    WEAPON_CATALOG,
    OFFHAND_CATALOG,
    ITEM_DEFS,
    CHOICE_ACTIONS,
    STAIR_ACTIONS,
  } = config;
  const {
    getState,
    getCurrentFloorState,
    ensureFloorExists,
    clamp,
    getHungerStateLabel,
    countPotionsInInventory,
    countFoodInInventory,
    useInventoryItem,
    renderSelf,
    setShowFloatingText,
    addMessage,
    moveToFloor,
    endTurn,
    resolvePotionChoice,
    setShowDeathModal,
    setShowChoiceModal,
    setHideChoiceModal,
    movePlayer,
    handleWait,
    debugRevealOrAdvanceStudio,
    debugAdvanceTimeline,
    tryCloseAdjacentDoor,
    quickUsePotion,
    closeContainerLoot,
    takeSelectedContainerLoot,
    takeAllContainerLoot,
    cycleHealingOverlay,
    closeHealingOverlay,
    useSelectedHealingConsumable,
    cycleTargetMode,
    enterTargetMode,
    cancelTargetMode,
    moveTargetCursor,
    selectTargetTile,
    confirmTargetAttack,
    createRuntimeId,
  } = runtime;
  const {
    getMainHand,
    getOffHand,
    getCombatWeapon,
  } = equipment;
  const {
    formatWeaponDisplayName,
    formatWeaponReference,
    formatWeaponStats,
    formatOffHandStats,
    getOffHandTooltipLines,
  } = presentation;
  const {
    formatRarityLabel,
    getItemModifierSummary,
    loadHighscores,
    loadLastHighscoreMarker,
    updateSavegameControls,
    hasLineOfSight,
    isStraightShot,
  } = core;
  const {
    boardElement,
    messageLogElement,
    inventoryListElement,
    heroSheetElement,
    inventoryFilterButtons,
    playerSheetElement,
    enemySheetElement,
    highscoreListElement,
    studioTopologyViewportElement,
    studioTopologySceneElement,
    studioTopologySummaryElement,
    studioTopologySelectorElement,
    studioTopologyMiniMapElement,
    studioTopologyModalElement,
    studioTopologyZoomRangeElement,
    studioTopologyZoomValueElement,
    studioTopologyPrevButtonElement,
    studioTopologyNextButtonElement,
    runStatsSummaryElement,
    runStatsKillsElement,
    hoverTooltipElement,
    choiceModalElement,
    choiceTitleElement,
    choiceTextElement,
    choiceDrinkButton,
    choiceStoreButton,
    choiceLeaveButton,
    containerLootModalElement,
    containerLootImageElement,
    containerLootTitleElement,
    containerLootSummaryElement,
    containerLootListElement,
    containerLootTakeSelectedButton,
    containerLootTakeAllButton,
    stairsModalElement,
    stairsTitleElement,
    stairsTextElement,
    stairsConfirmButton,
    stairsStayButton,
    deathModalElement,
    deathSummaryElement,
    resetStudioTopologyViewButtonElement,
  } = ui;

  const renderApi = createRenderApi({
    MIN_CRIT_CHANCE,
    MAX_CRIT_CHANCE,
    WIDTH,
    HEIGHT,
    TILE_SIZE,
    TILE_GAP,
    BOARD_PADDING,
    TILE,
    boardElement,
    messageLogElement,
    inventoryListElement,
    heroSheetElement,
    inventoryFilterButtons,
    playerSheetElement,
    enemySheetElement,
    highscoreListElement,
    studioTopologyViewportElement,
    studioTopologySceneElement,
    studioTopologySummaryElement,
    studioTopologySelectorElement,
    studioTopologyMiniMapElement,
    studioTopologyModalElement,
    studioTopologyZoomRangeElement,
    studioTopologyZoomValueElement,
    studioTopologyPrevButtonElement,
    studioTopologyNextButtonElement,
    runStatsSummaryElement,
    runStatsKillsElement,
    hoverTooltipElement,
    monsters: MONSTER_CATALOG,
    monsterNames: MONSTER_CATALOG.map((monster) => monster.name),
    itemNames: [
      "Heiltrank",
      ...Object.values(WEAPON_CATALOG).map((item) => item.name),
      ...Object.values(OFFHAND_CATALOG).map((item) => item.name),
      ...Object.values(ITEM_DEFS).map((item) => item.name),
    ],
    getState,
    getCurrentFloorState,
    getMainHand,
    getOffHand,
    getCombatWeapon,
    previewCombatAttack: (...args) => context.gameplay.previewCombatAttack?.(...args) ?? null,
    hasLineOfSight,
    isStraightShot,
    formatWeaponDisplayName,
    formatWeaponReference,
    formatWeaponStats,
    formatOffHandStats,
    getOffHandTooltipLines,
    formatRarityLabel,
    getItemModifierSummary,
    getHungerStateLabel,
    clamp,
    loadHighscores,
    loadLastHighscoreMarker,
    countPotionsInInventory,
    countFoodInInventory,
    useInventoryItem,
    renderSelf,
    selectTargetTile,
    confirmTargetAttack,
    createRuntimeId,
    resetStudioTopologyViewButtonElement,
  });
  setShowFloatingText(renderApi.showFloatingText);

  const modalController = createModalController({
    CHOICE_ACTIONS,
    STAIR_ACTIONS,
    getState,
    createSheetRow: renderApi.createSheetRow,
    updateSavegameControls,
    getCurrentFloorState,
    ensureFloorExists,
    returnToStartScreen: runtime.returnToStartScreen,
    renderSelf,
    addMessage,
    moveToFloor,
    endTurn,
    resolvePotionChoice,
    renderEquipmentCompareHtml,
    choiceModalElement,
    choiceTitleElement,
    choiceTextElement,
    choiceDrinkButton,
    choiceStoreButton,
    choiceLeaveButton,
    containerLootModalElement,
    containerLootImageElement,
    containerLootTitleElement,
    containerLootSummaryElement,
    containerLootListElement,
    containerLootTakeSelectedButton,
    containerLootTakeAllButton,
    stairsModalElement,
    stairsTitleElement,
    stairsTextElement,
    stairsConfirmButton,
    stairsStayButton,
    deathModalElement,
    deathSummaryElement,
    debugInfoModalElement: ui.debugInfoModalElement,
    debugAdvanceInputElement: ui.debugAdvanceInputElement,
    debugAdvanceButtonElement: ui.debugAdvanceButtonElement,
    debugInfoTextElement: ui.debugInfoTextElement,
    debugInfoStatusElement: ui.debugInfoStatusElement,
    debugAdvanceTimeline,
    formatWeaponDisplayName,
    formatWeaponStats,
    formatOffHandStats,
  });
  setShowDeathModal(modalController.showDeathModal);
  setShowChoiceModal(modalController.showChoiceModal);
  setHideChoiceModal(modalController.hideChoiceModal);

  const inputController = createInputController({
    getState,
    addMessage,
    confirmRestartRun: modalController.confirmRestartRun,
    resolveStairChoice: modalController.resolveStairChoice,
    cycleStairChoice: modalController.cycleStairChoice,
    resolvePotionChoice,
    cyclePotionChoice: modalController.cyclePotionChoice,
    toggleInventory: modalController.toggleInventory,
    toggleStudioTopology: modalController.toggleStudioTopology,
    toggleRunStats: modalController.toggleRunStats,
    toggleOptions: modalController.toggleOptions,
    toggleSavegames: modalController.toggleSavegames,
    toggleHelp: modalController.toggleHelp,
    toggleHighscores: modalController.toggleHighscores,
    toggleDebugInfo: modalController.toggleDebugInfo,
    triggerDebugAdvance: modalController.triggerDebugAdvance,
    closeStartModal: runtime.closeStartModal,
    movePlayer,
    handleWait,
    debugRevealOrAdvanceStudio,
    tryCloseAdjacentDoor,
    quickUsePotion,
    closeContainerLoot,
    takeSelectedContainerLoot,
    takeAllContainerLoot,
    cycleHealingOverlay,
    closeHealingOverlay,
    useSelectedHealingConsumable,
    cycleTargetMode,
    enterTargetMode,
    cancelTargetMode,
    moveTargetCursor,
    selectTargetTile,
    confirmTargetAttack,
  });

  return {
    ...renderApi,
    ...modalController,
    ...inputController,
  };
}
